Ingredients
Sequence	Ingredient Name	Amount	Measurement
1	Sugar	0.75	Cup
2	Oil	0.25	Cup
3	Eggs	1	Unspecified
4	Flour	1.5	Cup
5	Baking Powder	1	Teaspoon
6	Cinnamon	1	Teaspoon
7	Milk	0.5	Cup

Instructions

Preheat oven to 350°F (175°C). Grease a mini muffin pan.

In a bowl, mix sugar, oil, and egg until smooth.

Add flour, baking powder, and cinnamon. Mix gently.

Pour in milk and stir just until combined.

Spoon batter into mini muffin cups until ¾ full.

Bake 10–12 minutes, until lightly golden.

Cool slightly before serving.